# example:
def increment(nr: float) -> float:
  return nr + 1

def decrement(nr: float) -> float:
  return nr - 1

def add(nr1: float, nr2: float) -> float:
  return nr1 + nr2

def substract(nr2: float, nr1: float) -> float:
  return nr2 - nr1

def multiply(nr1: float, nr2: float) -> float:
  return nr1 * nr2

def divide(nr1, nr2):
  if nr1 == 0 or nr2 == 0:
    return None
  else:
    return nr1 / nr2

